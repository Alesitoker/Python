 # 1.Definir una función max() que tome como argumento dos números y devuelva el mayor de ellos.
def max(num1, num2):
    if (num1 > num2):
        return num1
    else:
        return num2
    
print(max(1, 2))
print(max(26, 26))
print(max(10, 4))
